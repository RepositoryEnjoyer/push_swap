#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*aux;

	if (!*lst)
		return ;
	while (*lst)
	{
		aux = *lst;
		(*lst) = (*lst)->next;
		ft_lstdelone(aux, del);
	}
}
